<h5 class="toggle "><a href="#"><span class="arrow"></span>Comissão Eleitoral</a></h5><div class="toggle-content " style="">
<p>A Comissão Eleitoral coordenará e executará o Processo Eleitoral e a ela são atribuídas, dentre outras, as seguintes competências específicas:</p>
<p>• eleger o membro que exercerá a função de Secretário e aprovar os termos do Edital de divulgação do Processo Eleitoral;<br />
• decidir sobre dúvidas suscitadas com relação às eleições;<br />
• receber e examinar requerimento de inscrição de chapa e documentação pertinente;<br />
• homologar a inscrição das chapas;<br />
• proceder à apuração dos votos em reunião convocada para tanto;<br />
• homologar o resultado final da eleição; e<br />
• julgar as impugnações, denúncias e os recursos apresentados pelas chapas.</p>
<p>As reuniões da Comissão Eleitoral serão realizadas mediante convocação do seu Presidente ou de três de seus membros, em conjunto, bem como instaladas com a presença de, no mínimo, 3 membros, sendo um deles o Presidente. As decisões da Comissão Eleitoral serão lavradas em ata e tomadas pela maioria dos votos dos membros presentes, cabendo ao Presidente, além do seu, o voto de desempate.</p>
