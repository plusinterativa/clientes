<p><b>Inscrições de chapas encerradas!</b></p>
<div class="accordian">
<h5 class="toggle "><a href="#"><span class="arrow"></span>Quem pode concorrer</a></h5><div class="toggle-content " style="">
<p>O candidato deverá ser Participante ou Assistido de Plano SEBRAEPREV, bem como estar em pleno gozo de seus direitos regulamentares. Os candidatos deverão atender aos seguintes requisitos:</p>
<p>I &#8211; comprovada experiência no exercício de atividades nas áreas financeira, administrativa, contábil, jurídica, de fiscalização ou de auditoria;<br />
II &#8211; não ter sofrido condenação criminal transitada em julgado; e<br />
III &#8211; não ter sofrido penalidade administrativa por infração da legislação da Seguridade social ou como servidor público.</p>
<p>É vedada a candidatura de pessoas que sejam ligadas entre si por laços de parentesco, tanto por consanguinidade como por afinidade, até o quarto grau na linha reta ou colateral.<br />
É vedada, ainda, a candidatura simultânea aos Conselhos Deliberativo e Fiscal.</p>
</div>
</div>
<div class="accordian">
<h5 class="toggle "><a href="#"><span class="arrow"></span>Sobre a formação das chapas</a></h5><div class="toggle-content " style="">
<p>I – quanto ao Conselho Deliberativo:</p>
<p>a) 1 (um) membro titular e respectivo suplente, vinculado exclusivamente ao Patrocinador Fundador (Sebrae Nacional); e<br />
b) 3 (três) membros titulares e respectivos suplentes, vinculados a qualquer Patrocinador ou Instituidor.</p>
<p>II – quanto ao Conselho Fiscal: 2 (dois) membros titulares e respectivos suplentes, vinculados a qualquer Patrocinador ou Instituidor.</p>
<p>Cada chapa deverá apresentar candidatos a todas as vagas dos Conselhos Deliberativo e Fiscal.</p>
