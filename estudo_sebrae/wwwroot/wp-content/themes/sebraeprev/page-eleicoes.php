<p>A Comissão Eleitoral se reuniu na manhã desta sexta-feira (5), na sede do SEBRAE PREVIDÊNCIA, com a presença do Auditor Independente  da BDO Brazil, Luiz Leite, para a apuração dos votos da Eleição 2014. O processo  teve como objetivo escolher os novos membros dos Conselhos Deliberativo e Fiscal do SEBRAE PREVIDÊNCIA (membros representantes dos Participantes).</p>
<p>A chapa <strong>O Nosso Sebraprev</strong> venceu a eleição com <strong>1.230 votos</strong>. A chapa <strong>Sebraeprev + Perto de Você</strong> ficou com <strong>788 votos</strong>. Foram 2 votos em branco e 4 nulos. No total, 2.024 participantes do Plano SEBRAEPREV votaram.</p>
<p>Conforme previsto no Cronograma do Processo Eleitoral, a proclamação do resultado oficial ocorrerá no próximo dia 12 de dezembro.</p>
